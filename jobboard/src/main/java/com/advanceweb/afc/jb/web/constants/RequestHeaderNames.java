package com.advanceweb.afc.jb.web.constants;

/**
 * This interface is used to represent the names of standard HTTP Request header
 * parameters.
 * 
 * @author sukeshnambiar
 * 
 */
public interface RequestHeaderNames {
	/**
	 * The IP of the host machine initiating the request.
	 */
	String HOST = "host";

	/**
	 * The referrer URL that referred to the page
	 */
	String REFERER = "referer";

	/**
	 * The User agent used by the user.
	 */
	String USER_AGENT = "user-agent";
}
