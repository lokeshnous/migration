public class ResumeVisibilityDTO {
	
	/** The visibility id. */
	private String visibilityId;
	
	/** The visibility name. */
	private String visibilityName;
	
	/**
	 * Gets the visibility id.
	 *
	 * @return the visibility id
	 */
	public String getVisibilityId() {
		return visibilityId;
	}
	
	/**
	 * Sets the visibility id.
	 *
	 * @param visibilityId the new visibility id
	 */
	public void setVisibilityId(String visibilityId) {
		this.visibilityId = visibilityId;
	}
	
	/**
	 * Gets the visibility name.
	 *
	 * @return the visibility name
	 */
	public String getVisibilityName() {
		return visibilityName;
	}
	
	/**
	 * Sets the visibility name.
	 *
	 * @param visibilityName the new visibility name
	 */
	public void setVisibilityName(String visibilityName) {
		this.visibilityName = visibilityName;
	}
	
	

}
