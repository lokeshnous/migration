public class DropDownDTO {
	
	/** The option id. */
	private String optionId;
	
	/** The option name. */
	private String optionName;
	
	/** The net suite id. */
	private String netSuiteId;
	
	/**
	 * Gets the option id.
	 *
	 * @return the option id
	 */
	public String getOptionId() {
		return optionId;
	}
	
	/**
	 * Sets the option id.
	 *
	 * @param optionId the new option id
	 */
	public void setOptionId(String optionId) {
		this.optionId = optionId;
	}
	
	/**
	 * Gets the option name.
	 *
	 * @return the option name
	 */
	public String getOptionName() {
		return optionName;
	}
	
	/**
	 * Sets the option name.
	 *
	 * @param optionName the new option name
	 */
	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	
	/**
	 * Gets the net suite id.
	 *
	 * @return the net suite id
	 */
	public String getNetSuiteId() {
		return netSuiteId;
	}
	
	/**
	 * Sets the net suite id.
	 *
	 * @param netSuiteId the new net suite id
	 */
	public void setNetSuiteId(String netSuiteId) {
		this.netSuiteId = netSuiteId;
	}
	
}
