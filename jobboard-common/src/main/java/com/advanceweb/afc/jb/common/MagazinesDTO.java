public class MagazinesDTO {
	
	/** The magazine id. */
	private String magazineId;
	
	/** The magazine name. */
	private String magazineName;
	
	
	/**
	 * Gets the magazine id.
	 *
	 * @return the magazine id
	 */
	public String getMagazineId() {
		return magazineId;
	}
	
	/**
	 * Sets the magazine id.
	 *
	 * @param magazineId the new magazine id
	 */
	public void setMagazineId(String magazineId) {
		this.magazineId = magazineId;
	}
	
	/**
	 * Gets the magazine name.
	 *
	 * @return the magazine name
	 */
	public String getMagazineName() {
		return magazineName;
	}
	
	/**
	 * Sets the magazine name.
	 *
	 * @param magazineName the new magazine name
	 */
	public void setMagazineName(String magazineName) {
		this.magazineName = magazineName;
	}
	
	
}
